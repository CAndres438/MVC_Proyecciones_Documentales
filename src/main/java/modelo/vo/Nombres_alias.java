package modelo.vo;


public class Nombres_alias {
    private String  usuAlias;
    private String usuNombre;
    private String usuApellido;

   
    public String getUsuAlias() {
        return usuAlias;
    }

    /**
     * @param usuAlias the usuAlias to set
     */
    public void setUsuAlias(String usuAlias) {
        this.usuAlias = usuAlias;
    }

    /**
     * @return the usuNombre
     */
    public String getUsuNombre() {
        return usuNombre;
    }

    /**
     * @param usuNombre the usuNombre to set
     */
    public void setUsuNombre(String usuNombre) {
        this.usuNombre = usuNombre;
    }

    /**
     * @return the usuApellido
     */
    public String getUsuApellido() {
        return usuApellido;
    }

    /**
     * @param usuApellido the usuApellido to set
     */
    public void setUsuApellido(String usuApellido) {
        this.usuApellido = usuApellido;
    }
}


    
    
    
    
    
    
    

